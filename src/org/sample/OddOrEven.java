package org.sample;

public class OddOrEven {

	public static void main(String[] args) {
		int a=11;
		if (a%2==0) {
			System.out.println(a+" Even number print");
		}
		else {
			System.out.println(a+" add number print");
		}
		if (a%2==0) {
			System.out.println(a+" Even number print");
		}
		else {
			System.out.println(a+" add number print");
		}
	}
}
